#include "./libft.h"

int	ft_strcmp(char *s1, char *s2, int i)
{
	while (s1[i] != '\0' && s2[i] != '\n' && s1[i] == s2[i])
		i++;
	if (s1[i] == '\0' && s2[i] == '\n')
		return (1);
	return (-1);
}
